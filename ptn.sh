#!/bin/bash

# Обновление списка пакетов
# Команда apt update обновляет информацию о доступных пакетах
apt update

# Установка пакетов с сетевыми утилитами
# iputils-ping   — пакет, содержащий утилиту ping
# traceroute     — пакет для трассировки маршрута до узла
# net-tools      — пакет, содержащий утилиту netstat
apt install -y iputils-ping traceroute net-tools

# -------------------------
# Утилита ping
# -------------------------
# ping используется для проверки доступности узла в сети
# Принцип работы: отправляет ICMP Echo Request и ожидает Echo Reply
# Показывает задержку (в мс) и потери пакетов
# Пример использования:
# ping -c 4 8.8.8.8

# -------------------------
# Утилита traceroute
# -------------------------
# traceroute используется для определения маршрута следования пакетов
# Принцип работы: отправляет пакеты с увеличивающимся TTL,
# что позволяет определить каждый промежуточный маршрутизатор
# Пример использования:
# traceroute 8.8.8.8

# -------------------------
# Утилита netstat
# -------------------------
# netstat используется для отображения сетевых соединений,
# открытых портов и таблицы маршрутизации
# Пример использования:
# netstat -tuln   — показать прослушиваемые порты
# netstat -rn     — показать таблицу маршрутизации
# netstat -an     — показать все сетевые соединения

# Проверка, что утилиты установлены
ping -V
traceroute --version
netstat --version
